package exercicios;
/*

Operadores aritimetéticos: + - * / 

*/


public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("operações entre os números 15 e 7");
        System.out.println(" 15 + 7 = " + (15+7) );
        System.out.println(" 15 - 7 = " + (15-7) );
        System.out.println(" 15 * 7 = " + (15*7) );
        System.out.println(" 15 / 7 = " + (15/7) );
        System.out.println(" 15 / 7 = " + (15.0/7) ); //Para ter resultados em decimais, utilize números em decimais com . de separador



    }
}
